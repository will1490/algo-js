choice = Number(prompt("What is your favorite number between 1 and 7 ? :"));

switch (choice) {
    case 1:
      alert("It's Monday");
      break;
    case 2:
      alert("It's Tuesday");
      break;
    case 3:
      alert("It's Wednesday");
      break;
    case 4:
      alert("It's Thursday");
      break;
    case 5:
      alert("It's Friday");
    case 6:
      alert("It's Saturday");
    case 7:
      alert("It's Sunday");  
      break;
    default:
      alert("It's not a valid number ...");
      break;
  }

